package com.leo1;

public class in_de_oprater {

	public static void main(String[] args) {
		
		int a = 10;
		//increament 
		System.out.println("value to increment: "+(a)); 
		System.out.println("increment value store here : "+(a++));//post increament
		System.out.println("increment value show here : "+(a));
		System.out.println("pre increment: "+(++a));
		// decrement 
		System.out.println(a); 
		System.out.println(a--);
		System.out.println(a);
		System.out.println(--a);
		
		
		
		

	}

}
